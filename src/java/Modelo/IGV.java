package Modelo;

public class IGV {
private int idIGV;
private double Valor;
private Boolean Activo;

    public int getIdIGV() {
        return idIGV;
    }

    public void setIdIGV(int idIGV) {
        this.idIGV = idIGV;
    }

    public double getValor() {
        return Valor;
    }

    public void setValor(double Valor) {
        this.Valor = Valor;
    }

    public Boolean getActivo() {
        return Activo;
    }

    public void setActivo(Boolean Activo) {
        this.Activo = Activo;
    }
}
